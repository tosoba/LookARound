package com.lookaround.core.android.appunta.orientation;

public class Orientation {
    private float x = 0f;
    private float z = 0f;
    private float y = 0f;

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getZ() {
        return z;
    }

    public void setZ(float z) {
        this.z = z;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }
}
